package org.fogbowcloud.sebal.model.image;

public class HOutput {
	private double H;
	private double a;
	private double b;
	private double rah;
	private double uAsterisk;
	private double L;
	
	public double getB() {
		return b;
	}
	
	public void setB(double b) {
		this.b = b;
	}

	public double getA() {
		return a;
	}

	public void setA(double a) {
		this.a = a;
	}

	public double getH() {
		return H;
	}

	public void setH(double h) {
		H = h;
	}

	public double getRah() {
		return rah;
	}

	public void setRah(double rah) {
		this.rah = rah;
	}

	public double getuAsterisk() {
		return uAsterisk;
	}

	public void setuAsterisk(double uAsterisk) {
		this.uAsterisk = uAsterisk;
	}

	public double getL() {
		return L;
	}

	public void setL(double l) {
		L = l;
	}
}
